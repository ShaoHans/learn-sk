using Common;

namespace PromptEngineering;

internal class P11_提取每个段落的主题 : IPromptRunner
{
    public async Task RunAsync()
    {
        var config = new OpenAIConfig
        {
            Template = @"<tag>标签内是一篇新闻，每个段落直接以空行隔开，请使用1个词语总结每个段落的主题。


